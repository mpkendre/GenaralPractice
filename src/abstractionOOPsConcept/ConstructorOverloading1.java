package abstractionOOPsConcept;

public class ConstructorOverloading1 {

	ConstructorOverloading1()
	{
		System.out.println("hi priya ");
	}
	
	ConstructorOverloading1(String a)
	{
		System.out.println("");
	}
	ConstructorOverloading1(String b , String c)
	{
		System.out.println("hushar priya");
	}
	public static void main(String[] args) 
	
	{
		new  ConstructorOverloading1();;
		
		new ConstructorOverloading1(   "   ");
		
		new ConstructorOverloading1(" ","  ");

		
		
	
	
	}
	
}
